package weihua.realme;

/**
 * 人生
 * 四件事（探索自我、适应生活、耕耘事业、处理工作）
 * @author chengwei2
 *
 */
public interface Life {
	/**
	 * 探索自我(主动、向内)
	 */
	public void exploreInnerMe();

	/**
	 * 耕耘事业(主动、向外)
	 */
	public void cultivateCareer();

	/**
	 * 适应生活(被动、向外)
	 */
	public void adaptLife();

	/**
	 * 处理工作(被动、向外)
	 */
	public void dealWork();
}
